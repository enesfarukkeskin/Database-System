    // This function used for loading the instance to our program's table from the database.
    // This function is used for getting the instance from the database.
      public void Subject_Load() {
        int c;
        try {
            pst = con.prepareStatement("CALL `getSubject`();");
            rs = pst.executeQuery();
            
            ResultSetMetaData rsd = rs.getMetaData();
            c = rsd.getColumnCount();
            d = (DefaultTableModel)jTable1.getModel();
            d.setRowCount(0);
            while(rs.next()) {
                Vector v2 = new Vector();
                for(int i = 1; i <= c; i++) {
                    v2.add(rs.getString("sid"));
                    v2.add(rs.getString("subjectname"));
                }
                d.addRow(v2);
            }
                    
        } catch (SQLException ex) {
            Logger.getLogger(user.class.getName()).log(Level.SEVERE, null, ex);
        }
      }

