// This function used for clicking the 'Create' button and creating an instance.
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            String subject = txtsubject.getText();
            
            // This line used for adding the created instance into the MySQL
            pst = con.prepareStatement("CALL insertSubject(?);");
            pst.setString(1, subject);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Subject is Created");
            txtsubject.setText("");
            Subject_Load();
            txtsubject.requestFocus();
        } catch (SQLException ex) {
            Logger.getLogger(user.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

